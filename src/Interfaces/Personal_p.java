    private void cargaTabla(Connection co) {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
//    Connection co = wv.getCon();
        DefaultTableModel modelo;
//        JTable tabla = new JTable(modelo);
        modelo = (DefaultTableModel) jTable1.getModel();
        int a = modelo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
//            System.out.println("i " + i);
            modelo.removeRow(i);
        }

//        TableModel modelo = jTable1.getModel();
        try {
            Statement st = co.createStatement();
            ResultSet rs = st.executeQuery("select * from Personal");
            // Bucle para cada resultado en la consulta
//            for (int x = 1; x <= rs.getMetaData().getColumnCount(); x++) {
//                System.out.print(rs.getMetaData().getColumnName(x) + "\t");
//                System.out.print(rs.getMetaData().getColumnTypeName(x) + "\t");
//
//            }
            while (rs.next()) {
                // Se crea un array que será una de las filas de la tabla.
                int colu = 17;
                Object[] fila = new Object[colu]; // Hay tres columnas en la tabla

                // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                for (int i = 0; i < colu - 1; i++) {
                    switch (i){
                        case 10:
                            fila[i] = rs.getObject(i + 1); 
                            break;
                            
                        default:
                            fila[i] = rs.getObject(i + 1); // El primer indice en rs es el 1, no el cero, por eso se suma 1.
                    }
                    
                }
                // Se añade al modelo la fila completa.
                modelo.addRow(fila);
            }
            jTable1.setModel(modelo);

        } catch (SQLException ex) {
//            Logger.getLogger(Tipos_doc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

