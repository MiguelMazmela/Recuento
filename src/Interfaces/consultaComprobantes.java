    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        tipo = JCBTiposDocs.getSelectedItem().toString();
        serie = JCBSeries.getSelectedItem().toString();
        numero = JTFNumero.getText();
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jTable1.getModel();
        int a = modelo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(i);
        }
        sql = "SELECT cnumero_documento_item,articulo,cunidad_de_medida_consumo,"
                + "ncantidad_consumo,nvalor_venta,nigv,nimporte_total_a,ruc,dni ,cliente, ctransportista,cubigeo,"
                + "dfecha_documento,df_ref "
                + "FROM historia "
                + "WHERE ctipo_documento_id=? and cserie_documento=? AND cnumero_documento=?";
        String doc = serie + "-" + numero;
        double tnvv = 0;

        try {
            PreparedStatement psm = wv.conectar().prepareStatement(sql);
            psm.setString(1, tipo);
            psm.setString(2, serie);
            psm.setString(3, doc);
            ResultSet rs = psm.executeQuery();
            int colu = 10;
            Object[] fila = new Object[colu]; // Hay tres columnas en la tabla
            DecimalFormat formatea = new DecimalFormat("###,##0.00");

            while (rs.next()) {
                fila[0] = rs.getString(1);
                fila[1] = rs.getString(2).substring(0, rs.getString(2).indexOf("-"));
                fila[2] = rs.getString(2).substring(rs.getString(2).indexOf("-") + 1, rs.getString(2).length());
                fila[3] = rs.getString(3);
                fila[4] = formatea.format(rs.getDouble(4));
                fila[5] = formatea.format(rs.getDouble(5) / rs.getDouble(4));
                fila[6] = formatea.format(rs.getDouble(5));
                fila[7] = formatea.format(rs.getDouble(6));
                fila[8] = formatea.format(rs.getDouble(7));
                JTFCliente.setText(rs.getString("cliente"));
                jTextField1.setText(rs.getString("ruc"));
                

                tnvv = tnvv + rs.getDouble(5);
                jFormattedTextField3.setValue(tnvv);
                modelo.addRow(fila);
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(consultaComprobantes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


