    private void carga_lotes() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        DecimalFormat formatea = v.MyFormatter();
        DefaultTableModel modelo;
//        JTable tabla = new JTable(modelo);
        modelo = (DefaultTableModel) jTable2.getModel();
        v.borratabla(modelo);
        String sql = "SELECT * FROM LOTES";
        try {
            PreparedStatement ps = v.getCon().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            int colu = 5;

            Object[] fila = new Object[colu];

            while (rs.next()) {
                fila[0] = rs.getString("LOTE");
                fila[1] = rs.getString("CART_ID");
                fila[2] = rs.getString("CART_NOM");
                fila[3] = rs.getString("DFECHA_VENCIMIENTO");
                fila[4] = rs.getDouble("CUBICACION");
                modelo.addRow(fila);
            }
            jTable2.setModel(modelo);

        } catch (SQLException ex) {
            Logger.getLogger(lotes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

