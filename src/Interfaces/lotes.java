    private void graba_datos() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String sSQL = "insert into LOTES ("
                + "LOTE,CART_ID,CART_NOM,DFECHA_VENCIMIENTO,CUBICACION)"
                + "values(?,?,?,?,?)";
        try {
            PreparedStatement ps = v.conectar().prepareStatement(sSQL);
            ps.setString(1, Tlote.getText().toUpperCase());
            ps.setString(2, Tcodigo.getText().toUpperCase());
            ps.setString(3, Tdescripcion.getText().toUpperCase());
            ps.setDate(4, v.convert(Fecha_venc.getDate()));
            ps.setString(5, null);
            ps.executeUpdate();
            carga_lotes();
        } catch (SQLException ex) {
            Logger.getLogger(lotes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
